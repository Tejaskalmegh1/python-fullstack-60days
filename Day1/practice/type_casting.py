# Type casting in python:

var1 = 10
var2 = 20
var3 = 30

print("Before Type Casting : ")
print(" ")

print(type(var1))
print(type(var2))
print(type(var3))
print(" ")

print("After Type Casting : ")
print(" ")

var1 = str(10)
var2 = int(20)
var3 = float(30)

print(type(var1))
print(type(var2))
print(type(var3))
print(" ")